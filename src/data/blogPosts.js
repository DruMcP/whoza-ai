/**
 * Helper function to get all posts, sorted by date
 * @returns {Array} Sorted blog posts
 */
export function getAllPosts() {
  return [...blogPosts].sort((a, b) => new Date(b.publishDate) - new Date(a.publishDate));
}

/**
 * Helper function to get only published posts
 * @returns {Array} Published blog posts
 */
export function getPublishedPosts() {
  const allPosts = getAllPosts();
  const now = new Date();
  return allPosts.filter(post => {
    const publishDate = new Date(post.publishDate);
    publishDate.setHours(0, 0, 0, 0);
    const checkNow = new Date(now);
    checkNow.setHours(0, 0, 0, 0);
    return publishDate <= checkNow;
  });
}

export function getPostBySlug(slug) {
  return getPublishedPosts().find(post => post.slug === slug);
}

export function getFeaturedPosts() {
  return getPublishedPosts().filter(post => post.featured);
}

export function getRelatedPosts(currentPostSlug, category, limit = 2) {
  return getPublishedPosts()
    .filter(post => post.category === category && post.slug !== currentPostSlug)
    .slice(0, limit);
}
